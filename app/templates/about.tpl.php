<h3>About HECTOR</h3>
<p>Thank you for your interest in and use of HECTOR, the open source security 
intelligence platform designed by the University of Pennsylvania, School of 
Arts &amp; Sciences.
</p>
<p>
You can find full developer documentation under the app/docs directory.  You can
find full database documentation under the app/sql directory, including a PDF 
data model.
</p><p>
You can find HECTOR on the web at <a href="http://www.sas.upenn.edu/hector/">www.sas.upenn.edu/hector</a> 
and our code repository at <a href="https://github.com/madirish/hector">GitHub</a> where you can file bug reports 
or feature requests as <a href="https://github.com/madirish/hector/issues">issues</a>.
</p>